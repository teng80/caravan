import java.util.Scanner;

public class ExCalculation {

	public static void main(String[] args) {
		double i = 5;
		double j = 2;
		
		System.out.printf("두 수의 덧셈은 %.1f",i+j);
		System.out.println();
		System.out.printf("두 수의 뺄셈은 %.1f",i-j);
		System.out.println();
		System.out.printf("두 수의 덧셈은 %.1f",i*j);
		System.out.println();
		System.out.printf("두 수의 덧셈은 %.1f",i/j);
		

	}

}
