
public class Sum_for {
	public static void main(String[] args) {
		
		int sum1 = 0;
		
		for(int i = 1; i <= 10; i++) 
			sum1 += i;
		System.out.println("1부터 10까지의 합:" + sum1);
		
		System.out.println("=======================");
		
		int num = 1;
		int sum2 = 0;
		
		while(num<=10) {
			sum2 += num;
			num++;
		}
		System.out.println("1부터 10까지의 합:" + sum2);
		
		System.out.println("=======================");
		
		
		
	}
	}
	

